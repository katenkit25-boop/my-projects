// Роль 1: Маркетолог — собирает факты и цепляющие идеи по теме (использует веб-поиск).
import { askClaude } from "../lib/claude.js";

export async function marketer(topic) {
  const prompt = `Ты — маркетолог-исследователь. Тема будущего поста: «${topic}».
Используй веб-поиск, чтобы найти свежие факты, цифры и примеры.

Дай короткий бриф на русском:
- 5-7 ключевых фактов (по возможности с источником)
- 2-3 цепляющих угла/идеи для поста
- на кого рассчитан пост и какую эмоцию вызываем

Только бриф, без вступлений и воды.`;
  return askClaude(prompt, { webSearch: true });
}
