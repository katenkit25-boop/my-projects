// Роль 5: Финальный редактор — последняя проверка перед отправкой на согласование.
import { askClaude } from "../lib/claude.js";

export async function editor(htmlPost) {
  const prompt = `Ты — придирчивый редактор. Проверь готовый пост для Telegram:
- нет воды, штампов и повторов
- корректный HTML (только теги <b>, <i>, <a>), НЕ markdown
- один чёткий призыв к действию

Если всё хорошо — верни пост без изменений.
Если есть мелкие недочёты — исправь их сам.
Верни ТОЛЬКО финальный HTML-пост, без комментариев.

Пост:
${htmlPost}`;
  return askClaude(prompt);
}
