// Роль 2: Копирайтер — превращает бриф маркетолога в текст поста.
import { askClaude } from "../lib/claude.js";

export async function copywriter(topic, brief) {
  const prompt = `Ты — копирайтер. Напиши пост для Telegram-канала.
Тема: «${topic}».

Бриф от маркетолога:
${brief}

Требования:
- цепляющий первый абзац (без «в этой статье…» и приветствий)
- короткие абзацы по 1-3 предложения
- конкретика вместо воды; живой тон, обращение на «ты»
- один понятный призыв к действию в конце

Верни только текст поста, без пояснений.`;
  return askClaude(prompt);
}
